package intalio.cts.mobile.android.data.network.response;

public class DataItem{
	private boolean isSystem;
	private String ar;
	private String en;
	private int id;
	private String keyword;
	private String fr;

	public boolean isIsSystem(){
		return isSystem;
	}

	public String getAr(){
		return ar;
	}

	public String getEn(){
		return en;
	}

	public int getId(){
		return id;
	}

	public String getKeyword(){
		return keyword;
	}

	public String getFr(){
		return fr;
	}
}
